//------------------------------REQUIRE------------------------------
const express = require('express');
const app = express();

//------------------------------CONFIG------------------------------

app.set('port', process.env.PORT || 4000);

//------------------------------MIDDLEWARES------------------------------

app.use(express.json());

//------------------------------DB------------------------------

//------------------------------ROUTES------------------------------

//------------------------------SERVER------------------------------

app.listen(app.get('port'), () => {
    console.log('Server on port ' + app.get('port'));
})